    public function store(Request $request)
    {
        try {
            $deadline = $request->deadline;
            $deadline = date('Y-m-d H:i:s',strtotime($deadline));
            $localToUtc = localToUtc($deadline,get_local_time())->toDateTimeString();
            $request->request->add(['deadline'=>$localToUtc]);
            Todo::create($request->except('_token'));
            return redirect(route('todo.list'))->withSuccess('Record has been created');
        }catch (Exception $e){
            return redirect(route('todo.list'))->withError('Something went wrong . Please Try Again!');
        }
    }

